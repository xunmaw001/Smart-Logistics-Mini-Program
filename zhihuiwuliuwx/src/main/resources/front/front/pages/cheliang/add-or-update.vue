<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					cheliangUuidNumber: true,
					cheliangName: false,
					cheliangTypes: false,
					zhuangtaiTypes: false,
					cheliangContent: false,
					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					cheliangUuidNumber: this.getUUID(),//数字
					cheliangName: '',
					cheliangTypes: '',//数字
					cheliangValue: '',//数字对应的值
					zhuangtaiTypes: '',//数字
					zhuangtaiValue: '',//数字对应的值
					cheliangContent: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            cheliangTypesOptions: [],
            cheliangTypesIndex : 0,
            zhuangtaiTypesOptions: [],
            zhuangtaiTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let cheliangTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'cheliang_types',
            }
			let cheliangTypes = await this.$api.page(`dictionary`, cheliangTypesParams);
			this.cheliangTypesOptions = cheliangTypes.data.list
		/*下拉框*/
			let zhuangtaiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'zhuangtai_types',
            }
			let zhuangtaiTypes = await this.$api.page(`dictionary`, zhuangtaiTypesParams);
			this.zhuangtaiTypesOptions = zhuangtaiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`cheliang`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.cheliangId){
                this.ruleForm.cheliangId = options.cheliangId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            cheliangTypesChange(e) {
                this.cheliangTypesIndex = e.target.value
                this.ruleForm.cheliangValue = this.cheliangTypesOptions[this.cheliangTypesIndex].indexName
                this.ruleForm.cheliangTypes = this.cheliangTypesOptions[this.cheliangTypesIndex].codeIndex
            },
            zhuangtaiTypesChange(e) {
                this.zhuangtaiTypesIndex = e.target.value
                this.ruleForm.zhuangtaiValue = this.zhuangtaiTypesOptions[this.zhuangtaiTypesIndex].indexName
                this.ruleForm.zhuangtaiTypes = this.zhuangtaiTypesOptions[this.zhuangtaiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.cheliangUuidNumber)) {
                    this.$utils.msg(`车辆编号不能为空`);
                    return
                }
				if ((!this.ruleForm.cheliangName)) {
                    this.$utils.msg(`车辆名称不能为空`);
                    return
                }
				if ((!this.ruleForm.zhuangtaiTypes)) {
                    this.$utils.msg(`车辆状态不能为空`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`cheliang`, this.ruleForm);
                } else {
                    await this.$api.save(`cheliang`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
