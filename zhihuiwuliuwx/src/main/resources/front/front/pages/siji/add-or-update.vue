        async onLoad(options) {
		/*下拉框*/
			let sijiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'siji_types',
            }
			let sijiTypes = await this.$api.page(`dictionary`, sijiTypesParams);
			this.sijiTypesOptions = sijiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`siji`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.sijiId){
                this.ruleForm.sijiId = options.sijiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            sijiTypesChange(e) {
                this.sijiTypesIndex = e.target.value
                this.ruleForm.sijiValue = this.sijiTypesOptions[this.sijiTypesIndex].indexName
                this.ruleForm.sijiTypes = this.sijiTypesOptions[this.sijiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			sijiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.sijiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.username)) {
                    this.$utils.msg(`账户不能为空`);
                    return
                }
				if ((!this.ruleForm.sijiName)) {
                    this.$utils.msg(`司机名称不能为空`);
                    return
                }
				if ((!this.ruleForm.sijiPhone) && (!this.$validate.isMobile(this.ruleForm.sijiPhone))) {
                    this.$utils.msg(`联系方式不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.sijiEmail) && (!this.$validate.isEmail(this.ruleForm.sijiEmail))) {
                    this.$utils.msg(`邮箱不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.sijiPhoto)) {
                    this.$utils.msg(`司机头像不能为空`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`siji`, this.ruleForm);
                } else {
                    await this.$api.save(`siji`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
